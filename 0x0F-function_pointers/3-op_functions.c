/**
  * op_add - sum of 2 nums
  * @a: first num
  * @b: second num
  *
  * Description: suming 2 integers
  * Return: the sum
  */
int op_add(int a, int b)
{
		return (a + b);
}

/**
  * op_sub - subtract 2 nums
  * @a: first num
  * @b: second num
  *
  * Description: subtracting 2 integers
  * Return: the result
  */
int op_sub(int a, int b)
{
		return (a - b);
}

/**
  * op_mul - multiply 2 nums
  * @a: first num
  * @b: second num
  *
  * Description: multiplying 2 integers
  * Return: the result
  */
int op_mul(int a, int b)
{
		return (a * b);
}

/**
  * op_div - division of 2 numbers
  * @a: first num
  * @b: secong num
  *
  * Description: dividing 2 integers
  * Return: the result
  */
int op_div(int a, int b)
{
		return (a / b);
}

/**
  * op_mod - module of 2 nums
  * @a: first num
  * @b: second num
  *
  * Description: moduls of 2 integers
  * Return: the result
  */
int op_mod(int a, int b)
{
		return (a % b);
}

