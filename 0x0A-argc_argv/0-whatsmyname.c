#include "main.h"
#include <stdio.h>

/**
  * main - entry point
  * @argc: count of command lines arguments
  * @argv: list of command lines arguments
  *
  * Description: printing the program name
  * Return: zero to end the program
  */
int main(int __attribute__((unused))argc, char *argv[])
{
		printf("%s\n", argv[0]);
		return (0);
}
